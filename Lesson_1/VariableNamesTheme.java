public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");
        //цифра
        int number = 0;
        System.out.println(number);

        //сумма чисел
        int sum = 3 + 5;
        System.out.println(sum);

        //произведение цифр
        int product = 4 * 3;
        System.out.println(product);

        //максимальное число
        int maxNumber = 2_147_483_647;
        System.out.println(maxNumber);

        //количество десятков
        int tens = 99 / 10;
        System.out.println(tens);

        //вес собаки
        float dogWeight = 7.35f;
        System.out.println(dogWeight);

        //исходное число
        int originalNumber = 13;
        System.out.println(originalNumber);

        //процент по вкладу
        double percent = 6.66d;
        System.out.println(percent);

        //&
        char ampersand = '&';
        System.out.println(ampersand);

        //код ошибки
        String errorCode = "404";
        System.out.println(errorCode);

        //тип сообщения
        String messageType = "Warning";
        System.out.println(messageType);

        //число нулей
        int zeros = 0;
        System.out.println(zeros);

        //уникальное число
        int uniqueNumber = 777;
        System.out.println(uniqueNumber);

        //случайное число
        double randomNumber = 0.2333345d;
        System.out.println(randomNumber);

        //математическое выражение
        float expression = 1.345f - 0.354f;
        System.out.println(expression);

        //выбор
        int choice = 0;
        System.out.println(choice);

        //счет
        int score = 999;
        System.out.println(score);

        //максимальная длина
        float maxLength = 5.45f;
        System.out.println(maxLength);

        //пункт меню
        String menuItem = "Contacts";
        System.out.println(menuItem);

        //стоимость кофе на вынос
        int coffeeToGoCost = 6;
        System.out.println(coffeeToGoCost);

        //дата начала
        String startDate = "11.11.2021";
        System.out.println(startDate);

        //полное имя работника месяца
        String employeeOfMonthFullName = "Shishkin Ilya";
        System.out.println(employeeOfMonthFullName);

        //заголовок электронной книги
        String eBookTitle = "War";
        System.out.println(eBookTitle);

        //размер
        int size = 28;
        System.out.println(size);

        //вместимость
        float capacity = 43.234552f;
        System.out.println(capacity);

        //счетчик
        int counter = 1;
        System.out.println(counter);

        //путь до файла
        String filePath = "C://Desktop//text.txt";
        System.out.println(filePath);

        //количество чисел в каждой строке
        int numbersPerRow = 4;
        System.out.println(numbersPerRow);

        System.out.println("\n2. boolean-переменные");

        //сотни равны?
        boolean hundredsEqua = false;
        System.out.println(hundredsEqua);

        //компьютер включен?
        boolean computerOn = true;
        System.out.println(computerOn);

        //есть равные цифры?
        boolean digitsAreEqual = true;
        System.out.println(digitsAreEqual);

        //создано?
        boolean isCreated = false;
        System.out.println(isCreated);

        //пустое?
        boolean isEmpty = false;
        System.out.println(isEmpty);

        //активное?
        boolean isActive = true;
        System.out.println(isActive);

        //новый?
        boolean isNew = false;
        System.out.println(isNew);

        //электронная почта действительная
        boolean isValidEmail = false;
        System.out.println(isValidEmail);

        //имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println(hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");

        //старый universally unique identifier
        String oldUUId = "123e4567-e89b-12d3-a456-426655440000";
        System.out.println(oldUUId);

        //производитель оперативной памяти
        String manufacturerRam = "IRT";
        System.out.println(manufacturerRam);

        //емкость жесткого диска
        int capacityHdd = 2345;
        System.out.println(capacityHdd);

        //протокол передачи гипертекста
        String httpProtocol = "value";
        System.out.println(httpProtocol);

        //сокращенный uniform resource locator
        String shortUrl = "safe-surf.ru";
        System.out.println(shortUrl);

        //новый идентификатор клиента
        String newClientId = "111135";
        System.out.println(newClientId);

        //кодировка american standard code for information interchange
        String asciiEncoding = "65";
        System.out.println(asciiEncoding);
    }
}